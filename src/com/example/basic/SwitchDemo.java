package com.example.basic;

public class SwitchDemo {
    public static void main(String[] args) {
        int age = 10;
        switch (age) {
            case 5:
                System.out.println("age: 5");
                break;
            case 6:
                System.out.println("age: 6");
                break;
            case 7:
                System.out.println("age: 7");
                break;
            case 10:
                System.out.println("age: 10");
                break;
            default:
                System.out.println("age: 10");
                break;
        }
    }
}
